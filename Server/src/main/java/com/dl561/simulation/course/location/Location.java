package com.dl561.simulation.course.location;

import org.springframework.stereotype.Component;

@Component
public class Location {
    private float x;
    private float y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
